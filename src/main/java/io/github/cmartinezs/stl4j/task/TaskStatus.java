package io.github.cmartinezs.stl4j.task;

/**
 * The enum Task status.
 */
public enum TaskStatus {
    /**
     * Created task status.
     */
    CREATED,
    /**
     * Ready task status.
     */
    READY,
    /**
     * Waiting task status.
     */
    WAITING,
    /**
     * Executing task status.
     */
    EXECUTING,
    /**
     * Success task status.
     */
    SUCCESS,
    /**
     * Failed task status.
     */
    FAILED
}